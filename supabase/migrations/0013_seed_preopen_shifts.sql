    ('2026-06-17', '川島', '13:00', '21:00'),
    ('2026-06-17', '橋本', '13:00', '21:00'),
    ('2026-06-17', '桑原', '13:00', '18:00'),
    ('2026-06-18', '福田', '13:00', '21:00'),
    ('2026-06-18', '橋本', '13:00', '21:00'),
    ('2026-06-18', '二俣', '13:00', '16:00'),
    ('2026-06-18', '川島', '13:00', '16:00'),
    ('2026-06-19', '佐藤', '13:00', '21:00'),
    ('2026-06-19', '桑原', '13:00', '16:00'),
