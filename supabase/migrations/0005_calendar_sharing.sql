-- =====================================================================
-- スタッフ間のカレンダー共有のための調整
-- =====================================================================
-- (1) スタッフ画面のカレンダーで他スタッフの氏名・表示色を出すため、
--     ログイン済みユーザーは全プロフィールを「閲覧」できるようにする。
--     （更新・作成・削除は従来どおり本人/管理者のみ）
-- (2) スタッフ画面で承認済みの休み希望を「休」表示するため、
--     公開/確定期間の承認済み休み希望は、本人以外のスタッフも閲覧可にする。
-- =====================================================================

-- (1) profiles: 閲覧ポリシーを「本人のみ」→「ログイン済みは全員」に置き換え
drop policy if exists "本人は自分のプロフィールを閲覧" on profiles;
create policy "ログイン済みはプロフィールを閲覧"
  on profiles for select
  using (auth.uid() is not null);

-- (2) time_off_requests: 承認済みは他スタッフも閲覧可（カレンダー表示用）
create policy "承認済み休み希望は全員閲覧可"
  on time_off_requests for select
  using (status = 'approved' and auth.uid() is not null);
